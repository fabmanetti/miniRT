#include "../../include/miniRT.h"

double		point_dist(t_point p1, t_point p2)
{ 
	return (sqrt(pow((p2.x - p1.x), 2) + \
			pow((p2.y - p1.y), 2) + \
			pow((p2.z - p1.z), 2)));
}